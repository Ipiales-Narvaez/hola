public class Docentes {
    
    private static String idcoord; 
    private static String nomcoord; 
    private static String correocoord; 
    private static String telcoord; 
    private static String rol_semillero; 
    private static String usuarios_id_usu;
    
    public String getIdcoord() {
        return idcoord;
    }

    public void setIdcoord(String idcoord) {
        this.idcoord = idcoord;
    }

    public String getNomcoord() {
        return nomcoord;
    }

    public void setNomcoord(String nomcoord) {
        this.nomcoord = nomcoord;
    }

    public String getCorreocoord() {
        return correocoord;
    }

    public void setCorreocoord(String correocoord) {
        this.correocoord = correocoord;
    }

    public String getTelcoord() {
        return telcoord;
    }

    public void setTelcoord(String telcoord) {
        this.telcoord = telcoord;
    }

    public String getRol_semillero() {
        return rol_semillero;
    }

    public void setRol_semillero(String rol_semillero) {
        this.rol_semillero = rol_semillero;
    }

    public String getUsuarios_id_usu() {
        return usuarios_id_usu;
    }

    public void setUsuarios_id_usu(String usuarios_id_usu) {
        this.usuarios_id_usu = usuarios_id_usu;
    }
    
}
