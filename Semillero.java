public class Semillero {

    private static String idsemro;
    private static String nomsemro;
    private static String sedesemro;
    private static String correosemro;
    private static String coordinador_idcoord;

    public String getIdsemro() {
        return idsemro;
    }

    public void setIdsemro(String idsemro) {
        this.idsemro = idsemro;
    }

    public String getNomsemro() {
        return nomsemro;
    }

    public void setNomsemro(String nomsemro) {
        this.nomsemro = nomsemro;
    }

    public String getSedesemro() {
        return sedesemro;
    }

    public void setSedesemro(String sedesemro) {
        this.sedesemro = sedesemro;
    }

    public String getCorreosemro() {
        return correosemro;
    }

    public void setCorreosemro(String correosemro) {
        this.correosemro = correosemro;
    }

    public String getCoordinador_idcoord() {
        return coordinador_idcoord;
    }

    public void setCoordinador_idcoord(String coordinador_idcoord) {
        this.coordinador_idcoord = coordinador_idcoord;
    }

}
