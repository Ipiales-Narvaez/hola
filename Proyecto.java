public class Proyecto {

    private static String idpro;
    private static String nompro;
    private static String tipopro;
    private static String estpro;

    public static String getIdpro() {
        return idpro;
    }

    public void setIdpro(String idpro) {
        Proyecto.idpro = idpro;
    }

    public String getNompro() {
        return nompro;
    }

    public void setNompro(String nompro) {
        Proyecto.nompro = nompro;
    }

    public String getTipopro() {
        return tipopro;
    }

    public void setTipopro(String tipopro) {
        Proyecto.tipopro = tipopro;
    }

    public String getEstpro() {
        return estpro;
    }

    public void setEstpro(String estpro) {
        Proyecto.estpro = estpro;
    }

}
